# 5-5. Alien Colors #3: Version 1 (Alien is green)

print("--- Version 1: Alien is green ---")
alien_color = 'green' # এখানে 'green' সেট করা হয়েছে

if alien_color == 'green':
    print("Player earned 5 points.")
elif alien_color == 'yellow':
    print("Player earned 10 points.")
elif alien_color == 'red':
    print("Player earned 15 points.")
else:
    print("Unknown alien color.") # ভবিষ্যতের জন্য একটি else ব্লক যোগ করা হলো

